def Head():
    def half_circle(r):
        for theta in s.arange(0, pi, 0.001):
            x = r * cos(theta)
            y = r * sin(theta)
            glVertex(x, y)

    def half_circle_hair(r):
        for theta in s.arange(0, pi, 1):
            x = r * cos(theta)
            y = r * sin(theta)
            glVertex(x, y)

    glTranslate(0, 0.5, 0)
    glBegin(GL_POLYGON)
    # Skinny Color
    glColor3f(0, 0.4, 0.2)
    half_circle_hair(0.25)
    glEnd()
    glBegin(GL_LINE_STRIP)
    # Skinny Color

    glColor3f(0.8, 1, 1)
    half_circle(0.25)
    glEnd()

    # Eye
    def Eye():
        glTranslate(0.125, 0.125, 0)
        glBegin(GL_POLYGON)
        glColor3f(1, 1, 1)
        circle(0.05)
        glEnd()
        glBegin(GL_POLYGON)
        glColor3f(0, 0, 0)
        circle(0.02)
        glEnd()
        glTranslate(-0.125, -0.125, 0)

    Eye()
    glRotate(180, 0, 1, 0)
    Eye()
    glRotate(-180, 0, 1, 0)

    glTranslate(0, -0.5, 0)
